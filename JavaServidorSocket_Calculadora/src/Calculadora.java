public class Calculadora {
    public Calculadora() {
    }
    //Metodos da calculadora
    public double soma(double oper1, double oper2) {
        return oper1 + oper2;
    }

    public double subtrair(double oper1, double oper2) {
        return oper1 - oper2;
    }

    public double dividir(double oper1, double oper2) {
        return oper1 / oper2;
    }

    public double multiplicar(double oper1, double oper2) {
        return oper1 * oper2;
    }
}

